        function testDefaultAppDataLocator_Constructor_HappyPath(testCase)
            % Arrange
            [mockOS, ~] = testCase.createMock( ...
                ?mcpcoreserver.internal.facade.os.OSFacade, ...
                Strict=true ...
            );

            % Act
            locator = mcpcoreserver.internal.appdata.DefaultAppDataLocator( ...
                OSFacade=mockOS ...
            );

            % Assert
            testCase.verifyInstanceOf(locator, ?mcpcoreserver.internal.appdata.AppDataLocator, ...
                "DefaultAppDataLocator should be an AppDataLocator");
        end

        function testDefaultAppDataLocator_GetAppDataFolder_HappyPath_Linux(testCase)
            % Arrange
            import matlab.mock.actions.AssignOutputs

            [mockOS, behavior] = testCase.createMock( ...
                ?mcpcoreserver.internal.facade.os.OSFacade, ...
                Strict=true ...
            );

            expectedHome = fullfile("home", "user");

            when( ...
                behavior.ismac().withExactInputs(), ...
                AssignOutputs(false) ...
            );
            when( ...
                behavior.ispc().withExactInputs(), ...
                AssignOutputs(false) ...
            );
            when( ...
                behavior.isunix().withExactInputs(), ...
                AssignOutputs(true) ...
            );
            when( ...
                behavior.getenv("HOME"), ...
                AssignOutputs(expectedHome) ...
            );

            locator = mcpcoreserver.internal.appdata.DefaultAppDataLocator( ...
                OSFacade=mockOS ...
            );

            % Act
            path = locator.getAppDataFolder();

            % Assert
            expectedPath = fullfile(expectedHome, ".MathWorks", "MATLABMCPCoreServer");
            testCase.verifyEqual(path, expectedPath, ...
                "Linux path should be HOME/.MathWorks/MATLABMCPCoreServer");
        end

