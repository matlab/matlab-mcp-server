    methods (Test)
        function testDefaultPermissionManager_SetPermissionsToUserOnly_HappyPath_Unix_Folder(testCase)
            % Arrange
            import matlab.mock.actions.AssignOutputs

            [mockFS, fsBehavior] = testCase.createMock( ...
                ?mcpcoreserver.internal.facade.fs.FSFacade, ...
                Strict=true ...
            );
            [mockOS, osBehavior] = testCase.createMock( ...
                ?mcpcoreserver.internal.facade.os.OSFacade, ...
                Strict=true ...
            );

            expectedPath = fullfile("home", "user", ".MathWorks", "MATLABMCPCoreServer");
            attribs = struct("directory", true);
            expectedChmodCmd = sprintf("chmod 700 '%s'", expectedPath);

            when( ...
                osBehavior.ispc().withExactInputs(), ...
                AssignOutputs(false) ...
            );
            when( ...
                fsBehavior.fileattrib(expectedPath), ...
                AssignOutputs(true, attribs, '') ...
            );
            when( ...
                osBehavior.system(expectedChmodCmd), ...
                AssignOutputs(0, '') ...
            );

            fs = mcpcoreserver.internal.fs.internal.permissionmanager.DefaultPermissionManager( ...
                FSFacade=mockFS, ...
                OSFacade=mockOS ...
            );

            % Act
            fs.setPermissionsToUserOnly(expectedPath);

            % Assert
            testCase.verifyCalled( ...
                osBehavior.system(expectedChmodCmd), ...
                "Should call chmod with 700 for folders" ...
            );
        end

